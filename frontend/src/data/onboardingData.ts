export function getArtistsForLanguages(selectedLanguages: string[]): string[] {
  if (selectedLanguages.length === 0) {
    return ARTISTS_BY_LANGUAGE.hindi.slice(0, 10).concat(
      ARTISTS_BY_LANGUAGE.english.slice(0, 10)
    );
  }

  const seen = new Set<string>();
  const result: string[] = [];

  for (const lang of selectedLanguages) {
    const artists = ARTISTS_BY_LANGUAGE[lang];
    if (!artists) continue;
    for (const artist of artists) {
      if (!seen.has(artist)) {
        seen.add(artist);
        result.push(artist);
      }
    }
  }

  return result;
}
